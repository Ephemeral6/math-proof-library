"""Fit bias-only PEP: log(bias) vs log(T) -> exponent should be -1."""
import numpy as np

bias = [
    (0.0, 0.5, [3,5,8,10,12,15,20], [0.1250,0.0833,0.0556,0.0455,0.0385,0.0313,0.0238]),
    (0.0, 1.0, [3,5,8,10,12,15,20], [0.0714,0.0455,0.0294,0.0238,0.0200,0.0161,0.0122]),
    (0.5, 0.5, [3,5,8,10,12,15,20], [0.0952,0.0552,0.0333,0.0263,0.0217,0.0172,0.0128]),
    (0.5, 1.0, [3,5,8,10,12,15,20], [0.1112,0.0608,0.0325,0.0243,0.0193,0.0147,0.0106]),
    (0.9, 0.5, [3,5,8,10,12,15,20], [0.2665,0.4033,0.3081,0.3319,0.3023,0.2665,0.2237]),
    (0.9, 1.0, [3,5,8,10,12,15,20], [0.5735,0.4717,0.6569,0.6910,0.7819,0.8975,1.1064]),
]
print(f"{'beta':>5} {'etaL':>5} {'exp':>8} {'C in C/T':>10}")
for beta, etaL, Ts, vs in bias:
    # only fit the tail (last 5)
    Ts = np.array(Ts[-5:]); vs = np.array(vs[-5:])
    p, c = np.polyfit(np.log(Ts), np.log(vs), 1)
    # If exponent is -1, prefactor is exp(c)
    A = np.exp(c)
    print(f"{beta:5.2f} {etaL:5.2f}   T^{p:6.3f} {A:10.4f}")
